export const CONCEPTS_PER_AXIS = 5;
export const COURSE_LENGTH = BLOCKS.length * CONCEPTS_PER_AXIS; // 15

export function planFor(axis: string): LessonPlan[] {
  return PLAN_BY_AXIS[axis] ?? PLAN_BY_AXIS.marketing;
}

const PLAIN_LANGUAGE_RULE = `ЯЗЫК: простые слова, как для друга без бизнес-образования. ЗАПРЕЩЕНЫ аббревиатуры и жаргон: MRR, LTV, CAC, churn, retention, burn rate, UX, лиды, performance, юнит-экономика. Вместо них человеческие слова.`;

function lessonSystem(plan: LessonPlan, block: Block, cal: Calibration, niche: string | undefined, diagnosis: Diagnosis): string {
  return `Ты — автор короткого урока для основателя, который в этой теме новичок. Ниша: ${niche ?? cal.industry} (${cal.model}). Его слабое место: ${AXIS_NAMES[diagnosis.weakness.axis]} — ${diagnosis.weakness.title}, ${diagnosis.weakness.note}. Его сила: ${AXIS_NAMES[diagnosis.superpower.axis]} — ${diagnosis.superpower.title}.

Понятие урока: «${plan.term}». Главная мысль: ${plan.focus}.
Блок курса: «${block.title}».

${block.lens}

${PLAIN_LANGUAGE_RULE}

Общие правила:
- Обращайся на «ты», тепло, без менторства и лозунгов. Как будто объясняешь другу за кофе.
- В quiz ровно ${block.quiz} ${block.quiz === 1 ? "вопрос" : "вопроса"}. Оба варианта звучат разумно, но один явно лучше. explain (1 предложение) мягко объясняет, почему второй тоже выглядел логичным.
- scheme — простая схема из 2–4 коротких подписей (1–3 слова), покажем стрелками. Только если реально помогает; иначе [].
- Никаких выдуманных цифр: только числа из истории.

ИСТОРИЯ (используй её факты, не выдумывай новых):
${plan.anchor}

Верни ТОЛЬКО JSON: {"title":"заголовок урока под этот блок, до 50 знаков","stat":"главная цифра истории крупно, до 18 знаков","statNote":"что это за цифра, до 80 знаков просто","body":"текст по правилам блока","turn":"суть в 1 предложении","term":"${plan.term}","termNote":"объяснение понятия бытовым примером","scheme":["подпись","подпись"],"examples":[{"case":"что случилось, кратко","why":"почему так вышло / почему сработало"}],"task":"задание на его продукте","quiz":[{"q":"...","left":"...","right":"...","correct":"left|right","explain":"..."}],"takeaway":"вывод урока одной строкой"}`;
}

// 15 уроков = 5 понятий × 3 блока. Грузим по блоку (5 уроков параллельно): одна сборка на блок, внутри листается без сети.
export async function runBlock(
  blockIndex: number,
  axis: string,
  cal: Calibration,
  niche: string | undefined,
  diagnosis: Diagnosis,
  usage: LlmUsage[],
): Promise<Lesson[]> {
  const b = Math.min(Math.max(blockIndex, 0), BLOCKS.length - 1);
  const plan = planFor(axis);
  const results = await Promise.all(
    plan.map((_, conceptIdx) => runLesson(b, conceptIdx, axis, cal, niche, diagnosis, usage)),
  );
  return results.sort((a, b2) => a.index - b2.index);
}

export async function runLesson(
  blockIndex: number,
  conceptIndex: number,
  axis: string,
  cal: Calibration,
  niche: string | undefined,
  diagnosis: Diagnosis,
  usage: LlmUsage[],
): Promise<Lesson> {
  const plan = planFor(axis);
  const b = Math.min(Math.max(blockIndex, 0), BLOCKS.length - 1);
  const c = Math.min(Math.max(conceptIndex, 0), plan.length - 1);
  const block = BLOCKS[b];

  const userMsg = `Диагноз фаундера: ${diagnosis.diagnosis}`;
  const lesson = await llmJson<Lesson>("assessor", lessonSystem(plan[c], block, cal, niche, diagnosis), userMsg, usage, 1600);

  lesson.index = b * CONCEPTS_PER_AXIS + c;
  lesson.total = COURSE_LENGTH;
  lesson.block = block.key;
  lesson.blockTitle = block.title;
  lesson.title = lesson.title || plan[c].title;
  lesson.scheme = Array.isArray(lesson.scheme)
    ? lesson.scheme.filter((x) => typeof x === "string" && x.trim()).slice(0, 4)
    : [];
  lesson.examples = Array.isArray(lesson.examples)
    ? lesson.examples.filter((e) => e?.case && e?.why).slice(0, 3)
    : [];
  lesson.quiz = (lesson.quiz ?? [])
    .filter((q) => q?.q && q?.left && q?.right)
    .slice(0, block.quiz)
    .map((q) => ({ ...q, correct: q.correct === "right" ? "right" : "left" }));
  return lesson;
}
